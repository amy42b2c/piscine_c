#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

int		main(void)
{
	int i;

	i = 'z';
	while (i >= 'a')
	{
		if (i % 2 == 0)
			ft_putchar(i);
		else
			ft_putchar(i - 32);
		i--;
	}
	ft_putchar('\n');
	return (0);
}
