#include <unistd.h>

int	main(int c, char **v)
{
	char n;
	char *s;

	n = '\n';
	if (c == 2)
	{
		s = v[1];
		while (*s && (*s== ' ' || *s == '\t'))
			s++;
		while (*s && (*s != ' ') && (*s != '\t'))
		{
			write(1, s, 1);
			s++;
		}
	}
	write(1, &n, 1);
	return (0);
}
