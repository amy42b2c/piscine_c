unsigned char	reverse_bits(unsigned char octet)
{
	int				i;
	unsigned char	temp;

	i = 0;
	while (i < 8)
	{
		temp |= ((octet >> i) & 1) << (7 - i);
		i++;
	}
	return (temp);
}
