#include <unistd.h>

int		main(int argc, char **argv)
{
	int		i;

	i = 0;
	if (argc != 2)
		write(1, "z\n", 2);
	else
	{
		while (argv[1][i] != '\0')
		{
			if (argv[1][i] == 'z')
				break;
			i++;
		}
		write(1, "z\n", 2);
	}
}
