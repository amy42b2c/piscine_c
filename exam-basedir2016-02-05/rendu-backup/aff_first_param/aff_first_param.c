#include <unistd.h>
#include <stdio.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

int		main(int ac, char **av)
{
	if (ac <= 1)
		return ('\n');
	ft_putstr(av[1]);

	return (0);
}
